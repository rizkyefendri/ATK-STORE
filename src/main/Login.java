    private void fullScreen(JPanel panel){
        Dimension sz = Toolkit.getDefaultToolkit().getScreenSize();
        panel.setSize(sz.width, sz.height);
    }
    
    public void changeUI(JPanel pn, JLabel lb, Color a, Color b){
        pn.setBackground(a);
        lb.setForeground(b);
    }
    
    public void setIcon(JLabel lb, String path){
        lb.setIcon(new javax.swing.ImageIcon(getClass().getResource(path)));
    }
    
    public void setBorder(JPanel pn, Color c, int a){
        Border border = BorderFactory.createLineBorder(c, a);
        pn.setBorder(border);
    }
    
    public void Warn(JPanel pn, Color c, JLabel lb, boolean x){
        pn.setBackground(c);
        lb.setVisible(x);
    }
 
