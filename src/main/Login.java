    private void loginAction() throws IOException{
        try{
            sql="SELECT users.username, users.password, pegawai.jabatan, pegawai.nama_pegawai, pegawai.foto FROM users JOIN pegawai ON users.kode_pegawai = pegawai.kode_pegawai where users.username='"+tuser.getText()+"' and users.password='"+tpass.getText()+"'";
            rs = stat.executeQuery(sql);
            if(rs.next()){
//                if(tuser.getText().equals(rs.getString("username"))&&tpass.getText().equals(rs.getString("password"))){
                    Alert al = new Alert("Login Success!", "/img/ok.png", new Color(33,191,115));
                    al.setVisible(true);
                    al.fadeOut(al);
                    al.setVisible(false);
                    fadeOut(this);
                    this.dispose();
                    new Dashboard(rs.getString("nama_pegawai"), rs.getString("jabatan"), rs.getBytes("foto")).setVisible(true);                                      
                }else{
                    Alert al = new Alert("Login Failed!", "/img/fail.png", new Color(255, 46, 49));
                    al.setVisible(true);
                    al.fadeOut(al);
                    al.setVisible(false);
                    tuser.setText("");
                    tpass.setText("");
                    tuser.requestFocus();
                } 
//            }
        } catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    public void fadeIn(JFrame fm){
        for (double i=0.0; i<=1.0; i=i+0.1){
            String val = i + "";
            float f = Float.valueOf(val);
            fm.setOpacity(f);
            try{
                Thread.sleep(20);
            }catch (Exception e){        
            }
        }        
    }
   
    public void fadeOut(JFrame fm){
        for (double i=1.0; i>=0.0; i=i-0.1){
            String val = i + "";
            float f = Float.valueOf(val);
            this.setOpacity(f);
            try{
                Thread.sleep(20);
            }catch (Exception e){        
            }
        }
    }
